    private void bnSupprimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnSupprimeActionPerformed
        // TODO add your handling code here:
        int response = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer cette activit√© ?");
        if (response == JOptionPane.YES_OPTION) {
            // R√©cup√©rer l'ID de l'activit√© s√©lectionn√©e
            int selectedRow = lisetActivite.getSelectedRow();
            if (selectedRow != -1) { // V√©rifier si une ligne est s√©lectionn√©e
                int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());

                // Supprimer l'activit√©
                if (as.delete(as.findById(id))) {
                    JOptionPane.showMessageDialog(this, "Activit√© supprim√©e avec succ√®s");
                    load(); // Actualiser le tableau
                } else {
                    JOptionPane.showMessageDialog(this, "Probl√®me lors de la suppression");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner une activit√© √† supprimer");
            }
        }
    }//GEN-LAST:event_bnSupprimeActionPerformed

    private void bnmodifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnmodifierActionPerformed
        // TODO add your handling code here:
        int response = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier cette activit√©?");
        if (response == JOptionPane.YES_OPTION) {
            // R√©cup√©rer les donn√©es de l'interface utilisateur
            int id = Integer.parseInt(model.getValueAt(lisetActivite.getSelectedRow(), 0).toString());
            String intitule = txtIntitule.getText();
            String description = txtDescription.getText();
            Date date = txtDate.getDate();

            // Mettre √† jour l'activit√©
            if (as.update(new Activite(id, intitule, date, description))) {
                JOptionPane.showMessageDialog(this, "Bien enregistr√©e");
                load(); // Actualiser le tableau
            } else {
                JOptionPane.showMessageDialog(this, "Probl√®me lors de la mise √† jour");
            }
        }
    }//GEN-LAST:event_bnmodifierActionPerformed

